#include <stdio.h>

/*****************************
问题：买书记录ISBN号去重并排序
解决方式一：桶排序 
******************************/

int main() {
	//假设 ISBN 号的为 0 - 1000 
	//n 表示有 n 个人登记买书 
	//t 表示书的 ISBN 号 
	int a[1001], i, n, t; 
	//对数组进行初始化 
	for (i = 0; i < 1001; i++) {
		a[i] = 0;
	}
	//获取要买书的人数
	scanf("%d",&n);
	//逐个获取要买的书的 ISBN 号
	for (i = 0; i < n; i++) {
		scanf("%d",&t);
		a[t] = 1;
	} 
	//展示结果 
	for (i = 0; i < 1001; i++) {
		if (a[i] == 1) {
			printf("%d ", i);
		} 
	}
	return 0;
}
